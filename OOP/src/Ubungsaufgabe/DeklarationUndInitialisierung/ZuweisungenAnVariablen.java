package Ubungsaufgabe.DeklarationUndInitialisierung;

public class ZuweisungenAnVariablen {
    public static void main(String[] ignored) {
        System.out.println(0);
        int zero = 0;
        System.out.println(zero);
        int one = 1;
        int whatThis = zero;
        System.out.println(whatThis);
        whatThis = one;
        System.out.println(whatThis);

    }
}
