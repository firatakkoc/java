package DataType;

public class PrimitiveDataType{
    public static void main(String[] args) {

        System.out.println("Byte Minimum  Value = "+Byte.MIN_VALUE+" Byte Maximum Value = "+Byte.MAX_VALUE);
        System.out.println("Short Minimum  Value = "+Short.MIN_VALUE+" Short Maximum Value = "+Short.MAX_VALUE);
        System.out.println("Integer Minimum  Value = "+Integer.MIN_VALUE+" Integer Maximum Value = "+Integer.MAX_VALUE);
        System.out.println("Long Minimum  Value = "+Long.MIN_VALUE+" Long Maximum Value = "+Long.MAX_VALUE);
        System.out.println("Float Minimum  Value = "+Float.MIN_VALUE+" Float Maximum Value = "+Float.MAX_VALUE);
        System.out.println("Double Minimum  Value = "+Double.MIN_VALUE+" Double Maximum Value = "+Double.MAX_VALUE);

        // char
        char harf = 'c';
        System.out.println("Harf: "+harf);

        // boolean
        boolean sonuc = true;
        System.out.println("Boolean Sonuc degeri; "+sonuc);

    }
}
